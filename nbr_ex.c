#include "ft_printf.h"

int	nbr_ex(int n)
{
	int	i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		i = 1;
	while (n)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
